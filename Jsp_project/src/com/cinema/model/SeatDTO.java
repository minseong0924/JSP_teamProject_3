package com.cinema.model;

public class SeatDTO {
	private int seatno;
	private int allseat;
	private int cinemacode;
	private int cincode;
	public int getSeatno() {
		return seatno;
	}
	public void setSeatno(int seatno) {
		this.seatno = seatno;
	}
	public int getAllseat() {
		return allseat;
	}
	public void setAllseat(int allseat) {
		this.allseat = allseat;
	}
	public int getCinemacode() {
		return cinemacode;
	}
	public void setCinemacode(int cinemacode) {
		this.cinemacode = cinemacode;
	}
	public int getCincode() {
		return cincode;
	}
	public void setCincode(int cincode) {
		this.cincode = cincode;
	}
	
	
}
